number = int(input("Enter number: "))
if number == 1:
    day = "Sunday"
elif number == 2:
    day = "Monday"
elif number == 3:
    day = "Tuesday"
elif number == 4:
    day = "Wednesday"
elif number == 5:
    day = "Thursday"
elif number == 6:
    day = "Friday"
elif number == 7:
    day = "Saturday"
else:
    day = "Enter correct number(1 to 7)."
print(day)
